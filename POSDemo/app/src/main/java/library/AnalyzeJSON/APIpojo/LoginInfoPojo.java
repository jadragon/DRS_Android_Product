package library.AnalyzeJSON.APIpojo;

public class LoginInfoPojo {
    String name;
    String en;
    String du_no;
    String dname;
    String s_no;
    String store;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEn() {
        return en;
    }

    public void setEn(String en) {
        this.en = en;
    }

    public String getDu_no() {
        return du_no;
    }

    public void setDu_no(String du_no) {
        this.du_no = du_no;
    }

    public String getDname() {
        return dname;
    }

    public void setDname(String dname) {
        this.dname = dname;
    }

    public String getS_no() {
        return s_no;
    }

    public void setS_no(String s_no) {
        this.s_no = s_no;
    }

    public String getStore() {
        return store;
    }

    public void setStore(String store) {
        this.store = store;
    }
}
