package library.AnalyzeJSON.APIpojo;

public class AttendanceContentPojo {
    int classnum;
    int commute;
    String note;
    String puch_time;
    String name;
    String en;
    String store;

    public int getClassnum() {
        return classnum;
    }

    public void setClassnum(int classnum) {
        this.classnum = classnum;
    }

    public int getCommute() {
        return commute;
    }

    public void setCommute(int commute) {
        this.commute = commute;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public String getPuch_time() {
        return puch_time;
    }

    public void setPuch_time(String puch_time) {
        this.puch_time = puch_time;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEn() {
        return en;
    }

    public void setEn(String en) {
        this.en = en;
    }

    public String getStore() {
        return store;
    }

    public void setStore(String store) {
        this.store = store;
    }
}
