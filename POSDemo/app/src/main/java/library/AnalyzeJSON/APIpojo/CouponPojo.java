package library.AnalyzeJSON.APIpojo;

public class CouponPojo {
    String mb_no;
    String mf_no;
    String mm_no;
    String name;
    String discount;
    String type;
    String money;

    public String getMb_no() {
        return mb_no;
    }

    public void setMb_no(String mb_no) {
        this.mb_no = mb_no;
    }

    public String getMf_no() {
        return mf_no;
    }

    public void setMf_no(String mf_no) {
        this.mf_no = mf_no;
    }

    public String getMm_no() {
        return mm_no;
    }

    public void setMm_no(String mm_no) {
        this.mm_no = mm_no;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDiscount() {
        return discount;
    }

    public void setDiscount(String discount) {
        this.discount = discount;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getMoney() {
        return money;
    }

    public void setMoney(String money) {
        this.money = money;
    }
}
